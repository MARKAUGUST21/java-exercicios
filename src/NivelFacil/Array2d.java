package NivelFacil;

public class Array2d {
    public static void main(String[] args) {

        String [] ninjas = new String[3];
        ninjas [0] = "Marcos";
        ninjas [1] = "Felipe";
        ninjas [2] = "Jose";
        System.out.println(ninjas[0]);

        for (int i = 0; i < ninjas.length; i++) {
            System.out.println(ninjas[i]);
            
        }
    }
}
